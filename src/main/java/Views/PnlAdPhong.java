    public void showData() throws ClassNotFoundException, SQLException {
        DefaultTableModel model = (DefaultTableModel) tbPhong.getModel();
        model.setRowCount(0); // Xóa các dòng cũ trong bảng

        RoomDAO roomDAO = new RoomDAO();
        List<Room> rooms = roomDAO.getAllRoom();

        for (Room room : rooms) {
            if(room.isDeleted())
                continue;
            Object row[] = new Object[3];
            row[0] = room.getRoomId();
            row[1] = room.getRoomName();
            row[2] = room.getRoomType();
            
            // Thêm dòng vào bảng
            model.addRow(row);
        }
    }
    public void resetData() {
        txtTenPhong.setText("");
        txtLoaiPhong.setText("");
    }
    
    public boolean checkTP(){
        if("".equals(txtTenPhong.getText())){
            lblErrorTP.setText("Tên phim bị trống");
            return false;
        }
        lblErrorTP.setText("");
        return true;
    }
    public boolean checkLP(){
        if("".equals(txtLoaiPhong.getText())){
            lblErrorLP.setText("Loại phòng bị trống");
            return false;
        }
        lblErrorLP.setText("");
        return true;
    }
    
    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(checkTP() && checkLP()){
             RoomDAO roomDAO = new RoomDAO();
             String name = txtTenPhong.getText();
             String type = txtLoaiPhong.getText();
            try {
                roomDAO.addRoom(name, type);
                resetData();
                showData();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(PnlAdPhong.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(PnlAdPhong.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

