    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int response = JOptionPane.showConfirmDialog(
            null,
            "Bạn có chắc chắn muốn xoá?",
            "Xác nhận",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE
        );

        if (response == JOptionPane.YES_OPTION) {
            // Hành động khi người dùng chọn Yes
            RoomDAO roomDAO = new RoomDAO();
            int id = Integer.parseInt(txtMaPhong.getText());
            
//            JOptionPane.showMessageDialog(this, String.valueOf(id), "Lỗi", JOptionPane.ERROR_MESSAGE);
            try {
                roomDAO.softDeleteById(id);
                resetData();
                showData();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(PnlAdPhong.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(PnlAdPhong.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else if (response == JOptionPane.NO_OPTION) {
            // Hành động khi người dùng chọn No
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        if(checkTP() && checkLP()){
            RoomDAO roomDAO = new RoomDAO();
            int id = Integer.parseInt(txtMaPhong.getText());
            String name = txtTenPhong.getText();
            String type = txtLoaiPhong.getText();
            try {
                roomDAO.updateCinemaById(id, name, type);
                resetData();
                showData();
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(PnlAdPhong.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(PnlAdPhong.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

